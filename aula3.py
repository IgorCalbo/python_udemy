"""
DocString

Python -> tipagem = Dinâmica / Forte 

Dinâmica - tipo de uma variável pode ser alterado durante a execução do código
Forte - necessidade da realização de cast
"""
print(1234) # o tipo do valor é atribuido dinamicamente pelo Python

# Strings são textos que estão dentro de aspas

# Aspas simples
print('Igor Calbo')
print(1, 'Igor "Calbo"')

# Aspas duplas
print("Igor Calbo")
print(2, "Igor 'Calbo'")

# Escape
print("Igor \"Calbo\"")

# r -> exibe o caracter de escape
print(r"Igor \"Calbo\"")
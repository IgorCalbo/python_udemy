# conversão de tipos -> linguagem dinâmica 

# print('1' + 1) # não converte os tipos sozinho, tipagem FORTE

# Polimorfismo, mesmo operador fazendo coisas diferentes
print(1 + 1) # somando
print("a" + "b") # concatenando 

# convertendo str para int
print(int("1") + 1)

# convertendo str para float
print(float("1") + 1)

# convertendo int para str
print(str(11) + 'b')